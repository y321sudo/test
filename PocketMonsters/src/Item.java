
public class Item {
//フィールド
	private String name=null;
	private int hp=0;
	private int level=0;


//コンストラクタ
	public String getName(){
		return name;
	}
	public void setName(String name){
		this.name=name;
	}

	public int getHp(){
		return hp;
	}
	public void setHp(int hp){
		this.hp=hp;
	}

	public int getLevel(){
		return level;
	}
	public void setLevel(int level){
		this.level=level;
	}


}
